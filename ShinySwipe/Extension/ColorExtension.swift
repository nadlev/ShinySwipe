import SwiftUI

extension Color {
  public static var backgroundGray: Color {
    Color(.backgroundGray)
  }

  public static var darkGray: Color {
    Color(.darkGray)
  }

  public static var shadowGray: Color {
    Color(.shadowGray)
  }

  public static var specialGray: Color {
    Color(.specialGray)
  }

  public static var lightGray: Color {
    Color(.lightGray)
  }

  public static var offWhite: Color {
    Color(.offWhite)
  }
}
